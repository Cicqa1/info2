# "=" - assign
a = 1
print(a) # 1

# "+=" - add and assign
a = 2
a += 3
print(a) # 5

# "-=" - subtract and assign
a = 5
a -= 2
print(a) # 3

# "*=" - multiply and assign
a = 3
a *= 4
print(a) # 12

# "/=" - divide and assign
a = 12
a /= 3
print(a) # 4.0

# "//=" - floor divide and assign
a = 13
a //= 3
print(a) # 4

# "%=" - modulo and assign
a = 13
a %= 5
print(a) # 3

# "**=" - power and assign
a = 2
a **= 3
print(a) # 8