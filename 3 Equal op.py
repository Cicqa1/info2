# "==" - equal operator
a = 5
b = 3
print(a == b) # False

# "!=" - not equal operator
a = 5
b = 3
print(a != b) # True

# "<" - less than operator
a = 5
b = 3
print(a < b) # False

# ">" - greater than operator
a = 5
b = 3
print(a > b) # True

# "<=" - less than or equal operator
a = 5
b = 3
print(a <= b) # False

# ">=" - greater than or equal operator
a = 5
b = 3
print(a >= b) # True